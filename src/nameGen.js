const generateLetters = () => {
  let cvar;
  let tvar;
  const letters = [];
  const letters2 = [];

  for (let i = 0; i < 26; i += 1) {
    cvar = 0;
    tvar = 0;
    letters[i] = [];
    for (let j = 0; j < 26; j += 1) {
      cvar += weights[i][j];
    }
    for (let j = 0; j < 26; j += 1) {
      tvar += weights[i][j];
      letters[i][j] = (tvar / cvar) * 1000;
    }
  }

  for (let h = 0; h < 26; h += 1) {
    letters2[h] = [];
    for (let i = 0; i < 26; i += 1) {
      cvar = 0;
      tvar = 0;
      letters2[h][i] = [];
      for (let j = 0; j < 26; j += 1) {
        cvar += weights2[h][i][j];
      }
      for (let j = 0; j < 26; j += 1) {
        tvar += weights2[h][i][j];
        letters2[h][i][j] = (tvar / cvar) * 1000;
      }
    }
  }

  return { letters, letters2 };
};

const alphalow = index => String.fromCharCode(97 + index);
const alphaup = index => String.fromCharCode(65 + index);
const rnd = ceiling => Math.floor(Math.random() * ceiling);

const genName = (len = getRandom(8, 12)) => {
  const { letters, letters2 } = generateLetters();
  const namelen = len;
  const curchar = rnd(26);
  let nam = alphaup(curchar);

  let ran;
  let curar;
  let firstchar;
  let secondchar;
  let nextchar;
  firstchar = curchar;
  ran = rnd(1000);
  secondchar = 0;
  curar = letters[firstchar];
  while (ran >= curar[secondchar]) secondchar += 1;
  nam += alphalow(secondchar);

  for (let cnt = 2; cnt < namelen; cnt += 1) {
    ran = rnd(1000);
    nextchar = 0;
    curar = letters2[firstchar][secondchar];
    while (ran >= curar[nextchar]) nextchar += 1;
    firstchar = secondchar;
    secondchar = nextchar;
    nam += alphalow(nextchar);
  }

  return nam;
};

